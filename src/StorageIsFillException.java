public class StorageIsFillException extends RuntimeException {

    public StorageIsFillException() {
    }

    public StorageIsFillException(String message) {
        super(message);
    }

    public StorageIsFillException(String message, Throwable cause) {
        super(message, cause);
    }

    public StorageIsFillException(Throwable cause) {
        super(cause);
    }

    public StorageIsFillException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
